    public void GetDataLevel()
    {
        LB_Level ObjLlevel =  (LB_Level) this.jComboBox_ID_Level.getSelectedItem();
        String id = ObjLlevel.getID_Level();
        this.jLabel_IDLevel.setText(id);
        
        if (this.jComboBox_ID_Level.getItemCount() > 0) 
        {
            int TotalLevelCredits = LBL.GetLevelCredits(id);
            int TotalSubjectCredits = LB.GetSubajectCredits(id);
            jLabel_TotalLavelCredits.setText(String.valueOf(TotalLevelCredits));
            jLabel_TotalSubjectCredits.setText(String.valueOf(TotalSubjectCredits));
            
            CalcCredits();
        }
        
    }
    
    private void CalcCredits()
    {
        try 
        {
            int Credts = Integer.parseInt(this.jSpinner_Credits.getValue().toString());
            int TotlLevelCredts = Integer.valueOf(jLabel_TotalLavelCredits.getText());
            int TotlSubjectCredts = Integer.valueOf(jLabel_TotalSubjectCredits.getText());

            int result = (Credts + TotlSubjectCredts);
            if (result > TotlLevelCredts) 
            {
                jButton_Save.setEnabled(false);
                JOptionPane.showMessageDialog(null, "The Credits of this Subject exceeds the Level Credits " + "(" + result + ")" , "AA Control - Subject", JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                jButton_Save.setEnabled(true);
            }
        } catch (Exception e) 
        {
            System.out.println(e.getMessage());
        }
    }

