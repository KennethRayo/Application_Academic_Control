    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:

        if (!"".equals(jTextField_IDSubject.getText()))
        {
            if (!"".equals(jTextField_Description.getText()))
            {
                if (jDateChooser_OpeningDate.getCalendar() != null)
                {
                    String IDSubject = jTextField_IDSubject.getText();
                    String Complement = "SUBJ_"+IDSubject;
                    String Status = jComboBox_Status.getSelectedItem().toString();
                    String Description = jTextField_Description.getText();

                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    String OpeningDate = dateFormat.format(jDateChooser_OpeningDate.getDate());

                    int Credits = Integer.parseInt(this.jSpinner_Credits.getValue().toString());
                    int MinimunNote = Integer.parseInt(this.jSpinner_MinimunNote.getValue().toString());
                    String Period = jComboBox_Period.getSelectedItem().toString();
                    int HoursDuration = Integer.parseInt(this.jSpinner_HoursDuration.getValue().toString());
                    int ExtraordinayMinimunNote = Integer.parseInt(this.jSpinner_ExtraordinayMinimunNote.getValue().toString());
                    int MinimunQuote = Integer.parseInt(this.jSpinner_MinimunQuote.getValue().toString());
                    String TypeOfCurrency = jComboBox_TypeOfCurrency.getSelectedItem().toString();
                    int ReprobingAbsence = Integer.parseInt(this.jSpinner_ReprobingAbsence.getValue().toString());
                   
                    //String CIFType = jComboBox_CIFType.getSelectedItem().toString();
                    LB_Level ObjLlevel =  (LB_Level) this.jComboBox_ID_Level.getSelectedItem();
                    String IDLevel = ObjLlevel.getID_Level();

                    if (Actualizar == true)
                    {
                        //UPDATE

                        LB = new LB_Subject(ID, IDLevel, OpeningDate, Status, Description, 
                                Credits, MinimunNote, MinimunQuote, TypeOfCurrency, 
                                Period, ReprobingAbsence, HoursDuration, ExtraordinayMinimunNote, 
                                ApplyScholarshipDiscounts, DegreeTest, ProfessionalPractice,
                                AppliesUVirtualRetirement);

                        Response = LB.Update();
                        if (LB.eye > 0)
                        {
                            JOptionPane.showMessageDialog(null, "Subject " + LB.getID_Subject()+ "\n" + Response, "AA Control - Subject", JOptionPane.INFORMATION_MESSAGE);
                            //listar
                            //this.dispose();
                        }
                        Actualizar = false;
                    }
                    else
                    {
                        //SAVE

                        LB = new LB_Subject(Complement, IDLevel, OpeningDate, Status, Description, 
                                Credits, MinimunNote, MinimunQuote, TypeOfCurrency, 
                                Period, ReprobingAbsence, HoursDuration, ExtraordinayMinimunNote, 
                                ApplyScholarshipDiscounts, DegreeTest, ProfessionalPractice,
                                AppliesUVirtualRetirement);

                        //Insert CIF
                        String rptaRegistro = LB.Insert();

                        if (rptaRegistro != null)
                        {
                            JOptionPane.showMessageDialog(null, "Subject " + IDSubject + "\n" + rptaRegistro, "AA Control - Subject", JOptionPane.INFORMATION_MESSAGE);
                            //listar
                            //this.dispose();
                        }
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "The OpeningDate Is Required !", "AA Control - Subject", JOptionPane.WARNING_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "The Description Is Required !", "AA Control - Subject", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "The ID Is Required !", "AA Control - Subject", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_SaveActionPerformed

