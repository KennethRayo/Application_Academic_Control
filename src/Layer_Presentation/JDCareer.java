    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:

        if (!"".equals(jTextField_ID.getText()))
        {
            if (jComboBox_CIFType.getSelectedItem()!= null)
            {
               if (!"".equals(jTextField_Description.getText()))
                {
                    if (jDateChooser_OpeningDate.getCalendar() != null)
                    {
                       String IDCsreer = jTextField_ID.getText();
                       String Complement = "CARR_"+IDCsreer;
                       String State = jComboBox_State.getSelectedItem().toString();
                       String Description = jTextField_Description.getText();

                       SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                       String OpeningDate = dateFormat.format(jDateChooser_OpeningDate.getDate());

                       //String CIFType = jComboBox_CIFType.getSelectedItem().toString();
                       LB_Person PERDirect =  (LB_Person) this.jComboBox_CIFType.getSelectedItem();
                       String IDDirect = PERDirect.getID_Person();

                       if (Actualizar == true)
                       {
                           //UPDATE

                           LB = new LB_Career(ID, Description, IDDirect,
                               OpeningDate, State);

                           Response = LB.Update();
                           if (LB.eye > 0)
                           {
                               JOptionPane.showMessageDialog(null, "Career " + LB.getID_Career()+ "\n" + Response, "AA Control - Career", JOptionPane.INFORMATION_MESSAGE);
                               //listar
                               //this.dispose();
                           }
                           Actualizar = false;
                       }
                       else
                       {
                           //SAVE
                           
                           LB = new LB_Career(Complement, Description, IDDirect,
                               OpeningDate, State);

                           //Insert CIF
                           String rptaRegistro = LB.Insert();

                           if (rptaRegistro != null)
                           {
                               JOptionPane.showMessageDialog(null, "Career " + Complement + "\n" + rptaRegistro, "AA Control - Career", JOptionPane.INFORMATION_MESSAGE);
                               //listar
                               //this.dispose();
                           }
                        } 
                    }
                   else
                   {
                       JOptionPane.showMessageDialog(null, "The OpeningDate Is Required !", "AA Control - ClassRoom", JOptionPane.WARNING_MESSAGE);
                   }
                }
               else
               {
                   JOptionPane.showMessageDialog(null, "The Description Is Required !", "AA Control - ClassRoom", JOptionPane.WARNING_MESSAGE);
               }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "The IDDirector Is Required !", "AA Control - ClassRoom", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
       {
           JOptionPane.showMessageDialog(null, "The ID Is Required !", "AA Control - ClassRoom", JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_jButton_SaveActionPerformed

